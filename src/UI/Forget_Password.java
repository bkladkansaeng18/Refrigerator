    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
   
        if(!isEmail()){
            JOptionPane.showMessageDialog(null, "This is not valid email !!");
            jTextField1.setText("");
            jTextField1.requestFocusInWindow();
        }
        else{
            if(refrige.Refrige.forgetPass(jTextField1.getText())){
              JOptionPane.showMessageDialog(null, "Password send to E-mail !!");
               this.dispose();
            new Login().setVisible(true);
            }else{
            JOptionPane.showMessageDialog(null, "Email not register !!");
            jTextField1.setText("");
            jTextField1.requestFocusInWindow();
            }

        }
    }//GEN-LAST:event_jButton3ActionPerformed

