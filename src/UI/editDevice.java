    private void updateData(){
        jLabel4.setText(user.getName());
        jLabel14.setText(refrig.getRefrig_id());
        textField1.setText(refrig.getRefrig_name());
        jComboBox1.setSelectedIndex(getIndexCombo(jComboBox1, refrig.getRefrig_band()));
        jComboBox2.setSelectedIndex(getIndexCombo(jComboBox2, refrig.getRefrig_color()));        
        jComboBox3.setSelectedIndex(getIndexCombo(jComboBox3, refrig.getRefrig_size()));
        setIconLabel(jLabel15, refrig);
    }
    private int getIndexCombo(JComboBox jCombo,String data){
        for(int i =0;i<100;i++){
            if(jCombo.getItemAt(i) == null) return -1;
            else if (jCombo.getItemAt(i).toString().equals(data)){
                return i;
            }
        }
        return -1;
    }
     private void setIconLabel(JLabel label, Models.Refrigerator refrid) {

        String band = refrid.getRefrig_band();
        String tmp = "";
        for (int i = 0; i < band.length(); i++) {
            if (band.charAt(i) == ' ') {
                break;
            }
            tmp += band.charAt(i);

        }
        label.setIcon(new javax.swing.ImageIcon(getClass().getResource("/UI/Static/Refrigerator/" +tmp + "-icon.png")));
  
    }
